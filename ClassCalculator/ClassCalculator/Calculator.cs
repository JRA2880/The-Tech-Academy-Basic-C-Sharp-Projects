using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassCalculator
{
    class Calculator
    {

        public int Add (int a)
        {
            int b = a + a;
            return b;
        }
        public int Subtract (int a)
        {
            int b = a - a;
            return b;
        }
        public int Multiply(int a)
        {
            int b = a * a;
            return b;
        }
    }
}
